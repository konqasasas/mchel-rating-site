#!/usr/bin/env python3
"""Build the static Mchel Speedrun Rating website.

Inputs are generated by scripts/mchel_time_attack_rating_v1.py.  The output is a
plain HTML/CSS/JavaScript site that can be published from docs/ on GitHub
Pages.

Example:
  python build_site.py \
      --overall-csv work/rating/overall_ranking.csv \
      --components-csv work/rating/best30_components.csv \
      --metadata-json work/rating/rating_metadata.json \
      --snapshot-summary work/latest/mchel_1_12_2_run_summary_excluding_seasonal.json \
      --out-dir docs
"""
from __future__ import annotations

import argparse
import csv
import html
import json
import shutil
from collections import defaultdict
from datetime import datetime, timedelta, timezone
from pathlib import Path
from typing import Any
from urllib.parse import quote

JST = timezone(timedelta(hours=9), name="JST")
SITE_TITLE = "ちぇる鯖アスレチック 総合レートランキング"
SITE_DESCRIPTION = "Mchel 1.12.2アスレチックの公開スピードランランキングから算出した、非公式の総合レートランキング。"


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(description="Build static Mchel rating pages.")
    parser.add_argument("--overall-csv", type=Path, required=True)
    parser.add_argument("--components-csv", type=Path, required=True)
    parser.add_argument("--metadata-json", type=Path, required=True)
    parser.add_argument("--snapshot-summary", type=Path, default=None)
    parser.add_argument("--out-dir", type=Path, default=Path("docs"))
    return parser.parse_args()


def read_csv(path: Path) -> list[dict[str, str]]:
    with path.open("r", encoding="utf-8-sig", newline="") as f:
        return list(csv.DictReader(f))


def escape(value: Any) -> str:
    return html.escape(str(value), quote=True)


def float_value(value: str | None, fallback: float = 0.0) -> float:
    try:
        return float(value or fallback)
    except (TypeError, ValueError):
        return fallback


def int_value(value: str | None, fallback: int = 0) -> int:
    try:
        return int(float(value or fallback))
    except (TypeError, ValueError):
        return fallback


def format_time(ms: int) -> str:
    minutes, remainder = divmod(ms, 60_000)
    seconds, millis = divmod(remainder, 1_000)
    return f"{minutes}:{seconds:02d}.{millis:03d}"


def format_jst(value: str | None) -> str:
    if not value:
        return "取得日時不明"
    try:
        parsed = datetime.fromisoformat(value.replace("Z", "+00:00"))
        if parsed.tzinfo is None:
            parsed = parsed.replace(tzinfo=timezone.utc)
        return parsed.astimezone(JST).strftime("%Y年%-m月%-d日 %H:%M JST")
    except ValueError:
        return escape(value)


def write_text(path: Path, text: str) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(text, encoding="utf-8")


def unofficial_notice() -> str:
    return """
<div class="unofficial-notice" role="note">
  <div class="notice-inner">
    <strong>非公式</strong>
    <span>Mchel公式とは関係ありません。</span>
  </div>
</div>
"""


def unofficial_chip(compact: bool = False) -> str:
    cls = "unofficial-chip compact" if compact else "unofficial-chip"
    return f'<div class="{cls}"><b>非公式</b></div>'


def nav(current: str = "") -> str:
    def link(href: str, label: str, key: str) -> str:
        cls = "nav-link active" if current == key else "nav-link"
        return f'<a class="{cls}" href="{href}">{label}</a>'

    return f"""
<header class="site-header">
  <div class="header-inner">
    <a class="brand" href="index.html" aria-label="トップへ">
      <span class="brand-mark">M</span>
      <span><strong>Mchel Speedrun Rating</strong><small>1.12.2 Athletic</small></span>
    </a>
    <nav class="main-nav" aria-label="メインナビゲーション">
      {link('index.html', '総合ランキング', 'ranking')}
      {link('about.html', 'レートについて', 'about')}
    </nav>
  </div>
</header>
"""


def footer(updated: str) -> str:
    return f"""
<footer class="site-footer">
  <div class="footer-inner">
    <p>非公式サイトです。記録データは公開ランキングAPIをもとに集計しています。</p>
    <p>最終データ取得：<time>{escape(updated)}</time></p>
  </div>
</footer>
"""


def page_shell(
    title: str,
    body: str,
    updated: str,
    current: str = "",
    depth: int = 0,
    description: str = "",
) -> str:
    prefix = "../" * depth
    header = (
        nav(current)
        .replace('href="index.html"', f'href="{prefix}index.html"')
        .replace('href="about.html"', f'href="{prefix}about.html"')
    )
    desc = description or SITE_DESCRIPTION
    return f"""<!doctype html>
<html lang="ja">
<head>
  <meta charset="utf-8">
  <meta name="viewport" content="width=device-width, initial-scale=1">
  <meta name="description" content="{escape(desc)}">
  <meta name="robots" content="index,follow">
  <title>{escape(title)} | {SITE_TITLE}</title>
  <link rel="stylesheet" href="{prefix}static/style.css">
  <script defer src="{prefix}static/app.js"></script>
</head>
<body>
{unofficial_notice()}
{header}
<main>
{body}
</main>
{footer(updated)}
</body>
</html>
"""


def tier_class(tier: str) -> str:
    """Return a CSS-safe class suffix for tiers such as B+, B-, and C-."""
    normalized = tier.lower().replace("+", "plus").replace("-", "minus")
    return "".join(ch for ch in normalized if ch.isalnum()) or "unknown"


def tier_badge(tier: str) -> str:
    return f'<span class="tier tier-{tier_class(tier)}">{escape(tier)}</span>'


def player_page_url(player_name: str) -> str:
    return "https://www.mchel.net/player/" + quote(player_name, safe="_-.")


def course_ranking_url(course_name: str) -> str:
    fragment = "athletic-1.12.2:ranking:" + quote(course_name, safe="")
    return "https://www.mchel.net/info#" + fragment


