def component_rank(comp: dict[str, str]) -> str:
    rank = int_value(comp.get("rank"))
    return f"#{rank}" if rank > 0 else "—"


def adjusted_score(comp: dict[str, str]) -> float:
    return float_value(comp.get("difficulty_adjusted_course_score"))


def weighted_contribution(comp: dict[str, str]) -> float:
    return float_value(comp.get("weighted_contribution"))


def build_player_page(player: dict[str, str], components: list[dict[str, str]], updated: str) -> str:
    rank = int_value(player.get("overall_rank"))
    rating = float_value(player.get("published_rating"))
    all_count = int_value(player.get("eligible_course_count"))
    ordered_components = sorted(components, key=lambda c: int_value(c.get("best30_position")))
    average_course_score = (
        sum(adjusted_score(comp) for comp in ordered_components) / len(ordered_components)
        if ordered_components
        else 0.0
    )
    first_place_count = sum(int_value(comp.get("rank")) == 1 for comp in ordered_components)

    records: list[str] = []
    for comp in ordered_components:
        course = comp.get("course_name", "")
        ranking_url = course_ranking_url(course)
        records.append(
            f"""
<tr>
  <td>{int_value(comp.get('best30_position'))}</td>
  <td><a href="{escape(ranking_url)}" target="_blank" rel="noopener noreferrer">{escape(course)}</a></td>
  <td class="course-rank">{component_rank(comp)}</td>
  <td>{format_time(int_value(comp.get('time_ms')))}</td>
  <td>{format_time(int_value(comp.get('course_record_time_ms')))}</td>
  <td>{float_value(comp.get('first_place_difficulty')):.3f}</td>
  <td>{float_value(comp.get('difficulty_multiplier')):.3f}×</td>
  <td>{adjusted_score(comp):.3f}</td>
  <td>{float_value(comp.get('weight')):.3f}</td>
  <td>{weighted_contribution(comp):.3f}</td>
</tr>"""
        )

    uuid = player["player_uuid"]
    mchel_profile_url = player_page_url(player.get("player_name", ""))
    body = f"""
<section class="player-hero">
  <div class="container">
    <a class="back-link" href="../index.html">← 総合ランキングへ戻る</a>
    <div class="player-heading">
      <div>
        {unofficial_chip(compact=True)}
        <p class="eyebrow">PLAYER PROFILE</p>
        <h1>{escape(player.get('player_name', ''))}</h1>
        <a class="player-link" href="{escape(mchel_profile_url)}" target="_blank" rel="noopener noreferrer">Mchelの個人ページを開く</a>
        <p class="uuid">UUID: <code>{escape(uuid)}</code></p>
      </div>
      <div class="hero-rating">
        {tier_badge(player.get('tier', ''))}
        <strong>{rating:,.2f}</strong>
        <span>総合 {rank} 位</span>
      </div>
    </div>
    <div class="profile-stats">
      <div><span>対象コース<br>ランクイン数</span><strong>{all_count}</strong></div>
      <div><span>最高<br>補正スコア</span><strong>{float_value(player.get('best_course_score')):.3f}</strong></div>
      <div><span>上位30コース<br>最低補正スコア</span><strong>{float_value(player.get('thirtieth_course_score')):.3f}</strong></div>
      <div><span>上位30コース<br>平均補正スコア</span><strong>{average_course_score:.3f}</strong></div>
      <div><span>採用コースの<br>1位回数</span><strong>{first_place_count}</strong></div>
    </div>
  </div>
</section>
<section class="container section">
  <div class="section-heading compact">
    <div>
      <p class="eyebrow">TOP 30 COURSES</p>
      <h2>上位30コース</h2>
      <p>総合レートの計算に採用された記録です。コース名からMchelのランキング画面を開けます。</p>
    </div>
  </div>
  <div class="table-wrap">
    <table class="components-table">
      <thead><tr><th>採用順位</th><th>コース</th><th>コース順位</th><th>自分のタイム</th><th>1位タイム</th><th>1位<br>難易度</th><th>難易度<br>倍率</th><th>補正<br>スコア</th><th>重み</th><th>重み付き<br>寄与</th></tr></thead>
      <tbody>{''.join(records)}</tbody>
    </table>
  </div>
  <div class="info-panel">
    <h3>このレートについて</h3>
    <p>総合レートは、対象コースで30件以上ランクインしたプレイヤーだけに表示されます。詳しい計算条件は<a href="../about.html">レートについて</a>をご覧ください。</p>
  </div>
</section>
"""
    return page_shell(
        f"{player.get('player_name', '')} の個人ページ",
        body,
        updated,
        depth=1,
        description=f"{player.get('player_name', '')} のMchel 1.12.2アスレチック総合レートと上位30コース。",
    )


