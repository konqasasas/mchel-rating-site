def page_shell(
    title: str,
    body: str,
    updated: str,
    current: str = "",
    depth: int = 0,
    description: str = "",
) -> str:
    prefix = "../" * depth
    header = (
        nav(current)
        .replace('href="index.html"', f'href="{prefix}index.html"')
        .replace('href="about.html"', f'href="{prefix}about.html"')
        .replace('href="courses.html"', f'href="{prefix}courses.html"')
    )
    desc = description or SITE_DESCRIPTION
    return f"""<!doctype html>
<html lang="ja">
<head>
  <meta charset="utf-8">
  <meta name="viewport" content="width=device-width, initial-scale=1">
  <meta name="description" content="{escape(desc)}">
  <meta name="robots" content="index,follow">
  <title>{escape(title)} | {SITE_TITLE}</title>
  <link rel="stylesheet" href="{prefix}static/style.css">
  <script defer src="{prefix}static/app.js"></script>
</head>
<body>
{unofficial_notice()}
{header}
<main>
{body}
</main>
{footer(updated)}
</body>
</html>
"""


def tier_class(tier: str) -> str:
    """Return a CSS-safe class suffix for tiers such as B+, B-, and C-."""
    normalized = tier.lower().replace("+", "plus").replace("-", "minus")
    return "".join(ch for ch in normalized if ch.isalnum()) or "unknown"


def tier_badge(tier: str) -> str:
    return f'<span class="tier tier-{tier_class(tier)}">{escape(tier)}</span>'


def player_page_url(player_name: str) -> str:
    return "https://www.mchel.net/player/" + quote(player_name, safe="_-.")


def course_ranking_url(course_name: str) -> str:
    fragment = "athletic-1.12.2:ranking:" + quote(course_name, safe="")
    return "https://www.mchel.net/info#" + fragment


