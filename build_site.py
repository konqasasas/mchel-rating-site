RANK_ORDER = ["EX", "S+", "S", "A+", "A", "B+", "B", "B-", "C+", "C", "C-", "D+", "D", "E"]


def build_rank_distribution(metadata: dict[str, Any]) -> str:
    """Render the official-player count by published rank as a compact bar list."""
    tier_counts_raw = metadata.get("tier_counts", {})
    tier_counts = {
        tier: int_value(str(tier_counts_raw.get(tier, 0)))
        for tier in RANK_ORDER
    }
    total = int_value(str(metadata.get("official_player_count", 0)))
    if total <= 0:
        total = sum(tier_counts.values())

    rows: list[str] = []
    for tier in RANK_ORDER:
        count = tier_counts[tier]
        percent = (100.0 * count / total) if total else 0.0
        rows.append(
            f"""
<div class="rank-distribution-row">
  <div class="rank-distribution-label">{tier_badge(tier)}</div>
  <div class="rank-distribution-track" aria-label="{escape(tier)} {count}人、{percent:.1f}%">
    <span class="rank-distribution-fill tier-fill-{tier_class(tier)}" style="width: {percent:.3f}%"></span>
  </div>
  <div class="rank-distribution-value"><strong>{count}</strong><span>人</span><small>{percent:.1f}%</small></div>
</div>"""
        )

    return f"""
<section class="rank-distribution" aria-labelledby="rank-distribution-title">
  <div class="rank-distribution-heading">
    <div>
      <p class="eyebrow">CURRENT DISTRIBUTION</p>
      <h2 id="rank-distribution-title">ランク別分布</h2>
      <p>現在の正式レート対象 <strong>{total}人</strong> を、公開ランクごとに表示しています。</p>
    </div>
  </div>
  <div class="rank-distribution-list">
    {''.join(rows)}
  </div>
</section>
"""

